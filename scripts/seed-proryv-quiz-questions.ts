async function main() {
  const lessonId = process.argv[2];
  const replace = process.argv.includes("--replace");

  if (!lessonId) {
    console.error(
      "Usage: npx tsx scripts/seed-proryv-quiz-questions.ts <lessonId> [--replace]"
    );
    process.exit(1);
  }

  const lesson = await prisma.lesson.findUnique({
    where: { id: lessonId },
    select: { id: true, title: true, type: true, content: true },
  });

  if (!lesson) {
    console.error(`Lesson ${lessonId} not found`);
    process.exit(1);
  }

  if (lesson.type !== "quiz") {
    console.error(
      `Lesson ${lessonId} has type "${lesson.type}", expected "quiz". Aborting.`
    );
    process.exit(1);
  }

  const content = (lesson.content as any) || {};
  const existing: Question[] = Array.isArray(content.questions)
    ? content.questions
    : [];

  const newIds = new Set(QUESTIONS.map((q) => q.id));
  const merged: Question[] = replace
    ? QUESTIONS
    : [...existing.filter((q) => !newIds.has(q.id)), ...QUESTIONS];

  await prisma.lesson.update({
    where: { id: lessonId },
    data: {
      content: { ...content, questions: merged },
    },
  });

  console.log(
    `[OK] Lesson "${lesson.title}" (${lessonId}): ${
      replace ? "replaced" : "appended"
    } ${QUESTIONS.length} questions. Total now: ${merged.length}.`
  );
}

main()
  .catch((err) => {
    console.error(err);
    process.exit(1);
  })
  .finally(() => prisma.$disconnect());
